package com.essar.pojos;

/**
 *
 * @author essaar
 */
public class AttDetails {
    long attendanceDetailsId;
    long attendanceId;
    long studentId;
    long classId;
    String studentName;
    String gender;
    String guardian;
    String phoneNumber;
    String status;

    public long getAttendanceDetailsId() {
        return attendanceDetailsId;
    }

    public void setAttendanceDetailsId(long attendanceDetailsId) {
        this.attendanceDetailsId = attendanceDetailsId;
    }

    public long getAttendanceId() {
        return attendanceId;
    }

    public void setAttendanceId(long attendanceId) {
        this.attendanceId = attendanceId;
    }

    public long getStudentId() {
        return studentId;
    }

    public void setStudentId(long studentId) {
        this.studentId = studentId;
    }

    public long getClassId() {
        return classId;
    }

    public void setClassId(long classId) {
        this.classId = classId;
    }

    public String getStudentName() {
        return studentName;
    }

    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getGuardian() {
        return guardian;
    }

    public void setGuardian(String guardian) {
        this.guardian = guardian;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
    

    
}
