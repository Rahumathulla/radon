    public void saveCustomerAccountItem(){
 
        CustomerAccountItem customerAccountItem = new CustomerAccountItem();
        customerAccountItem.setCustomerAccountItemName(txtParticularName.getText());
        customerAccountItem.setType(cmbType.getSelectedItem().toString());
        customerAccountItem.setRemarks(txtRemarks.getText());
        CustomerAccountItemDAO customerAccountItemDAO = new CustomerAccountItemDAO();
        customerAccountItemDAO.insertIntoDB(customerAccountItem);
        
    }
    public void updateCustomerAccountItem(){
 
        CustomerAccountItem customerAccountItem = new CustomerAccountItem();
        customerAccountItem.setCustomerAccountItemId(customerAccountItemId);
        customerAccountItem.setCustomerAccountItemName(txtParticularName.getText().trim());
        customerAccountItem.setType(cmbType.getSelectedItem().toString());
        customerAccountItem.setRemarks(txtRemarks.getText().trim());
        
        CustomerAccountItemDAO customerAccountItemDAO = new CustomerAccountItemDAO();
        customerAccountItemDAO.updateRecord(customerAccountItem);
        
    }
    
    
    public void renderDataIntoTable(List<CustomerAccountItem> customerAccountItemList){
        
        CustomerAccountItemModel = (DefaultTableModel) tblCustomerAccountItem.getModel();
        TableColumn column = tblCustomerAccountItem.getColumnModel().getColumn(tblCustomerAccountItem.getColumnCount()-1);
        column.setMaxWidth(0);
        column.setWidth(0);
        column.setPreferredWidth(0);
        lblCount.setText("Count : "+customerAccountItemList.size());
        //pricingTierModel.getColumnName(3).
        //System.out.println("-----"+pricingTierList.size());
        for(int i=0;i<customerAccountItemList.size();i++){
        CustomerAccountItemModel.addRow(new Object[]{customerAccountItemList.get(i).getCustomerAccountItemName(),customerAccountItemList.get(i).getType(),customerAccountItemList.get(i).getRemarks(),customerAccountItemList.get(i).getCustomerAccountItemId()});
        //model.
        }
        
        tblCustomerAccountItem.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent me) {
                if (me.getClickCount() == 2) {     // to detect doble click events
                    syncDataFromViewToTable();
                }
            }
        });
        
    }
    
        public void syncDataFromViewToTable(){
            CustomerAccountItem customerAccountItem = null;
            System.out.println("--------INSIDE Edit-"+tblCustomerAccountItem.getModel().getValueAt(tblCustomerAccountItem.getSelectedRow(),0));
            customerAccountItemId = Integer.parseInt(tblCustomerAccountItem.getModel().getValueAt(tblCustomerAccountItem.getSelectedRow(),tblCustomerAccountItem.getColumnCount()-1).toString());
            CustomerAccountItemDAO customerAccountItemDAO = new CustomerAccountItemDAO();
            customerAccountItem = customerAccountItemDAO.retrieveById(customerAccountItemId);
            System.out.println("--"+customerAccountItem.getCustomerAccountItemName());
            txtParticularName.setText(customerAccountItem.getCustomerAccountItemName());
            cmbType.setSelectedItem(customerAccountItem.getType());
            txtRemarks.setText(customerAccountItem.getRemarks());
            enableFormElements(true);
            btnSave.setText("Save");
            btnSave.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/save.png")));
        }

