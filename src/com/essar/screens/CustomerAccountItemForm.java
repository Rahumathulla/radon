    @Override
    public void actionPerformed(ActionEvent event) {
        try{
            JMenuItem menu = (JMenuItem) event.getSource();
            if (menu == mitDelete) {
              //txtParticularName= tblCustomerAccountItem.getSelectedRow();
                System.out.println("--------INSIDE DELETE-"+tblCustomerAccountItem.getModel().getValueAt(tblCustomerAccountItem.getSelectedRow(),0));
                String customerAccountItemId = tblCustomerAccountItem.getModel().getValueAt(tblCustomerAccountItem.getSelectedRow(),tblCustomerAccountItem.getColumnCount()-1).toString();
                long id = Long.parseLong(customerAccountItemId);
                CustomerAccountItemDAO customerAccountItemDAO = new CustomerAccountItemDAO();
                customerAccountItemDAO.deleteRecordById(id);
                GenericUtils.removeRowFromJTable(tblCustomerAccountItem, CustomerAccountItemModel);
            }
            if (menu == mitEdit) {
                //int rowIndex = tblSupplier.getSelectedRow();
                syncDataFromViewToTable();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }
