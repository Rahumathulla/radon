    public void enableFormElements(boolean flag){
        txtClassName.setEnabled(flag);
        txtFees.setEnabled(flag);
    }
    public boolean validateForm(){
        //btnSave.setText("Add");
        //btnSave.requestFocus();
        if(null == txtClassName.getText()){
            JOptionPane.showMessageDialog(null, "The class name "+txtClassName.getText() +" is not valid.");
            return false;
        }
        if(null == txtFees.getText() || !(txtFees.getText().matches("^\\d*\\.\\d+|\\d+|\\d+\\.\\d*$"))){
            JOptionPane.showMessageDialog(null, "Fees value "+txtFees.getText() +" is not valid.");
            return false;
        }
        //txtProductCategoryName.setText("");
        return true;
    }
    
    public void saveClassMasterForm(){ 
        ClassMaster classMaster = new ClassMaster();
        classMaster.setClassName(txtClassName.getText());
        classMaster.setFees(Double.parseDouble(txtFees.getText()));        
        ClassMasterDAO classMasterDAO = new ClassMasterDAO();
        classMasterDAO.insertIntoDB(classMaster);        
    }
    
    public void updateClassMasterForm(){ 
        ClassMaster classMaster = new ClassMaster();
        classMaster.setClassMasterId(classMasterId);
        classMaster.setClassName(txtClassName.getText());
        classMaster.setFees(Double.parseDouble(txtFees.getText()));
        ClassMasterDAO classMasterDAO = new ClassMasterDAO();
        classMasterDAO.updateClassMaster(classMaster);        
    }
    
    public void renderDataIntoTable(List<ClassMaster> classMasterList){
        //DefaultTableModel classMasterModel = null;
        classMasterModel = (DefaultTableModel) tblClassMaster.getModel();
        //System.out.println("-----"+classMasterList.size());
        for(int i=0;i<classMasterList.size();i++){
            classMasterModel.addRow(new Object[]{classMasterList.get(i).getClassName()
                    ,classMasterList.get(i).getFees()
                    ,classMasterList.get(i).getClassMasterId()});
        //model.
        }
        
        tblClassMaster.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent me) {
                if (me.getClickCount() == 2) {     // to detect doble click events
                    syncDataFromViewToTable();
                }
            }
        });
        
    }
    
    public void syncDataFromViewToTable(){
        ClassMaster classMaster= null;
        System.out.println("--------INSIDE Edit-"+tblClassMaster.getModel().getValueAt(tblClassMaster.getSelectedRow(),0));
        classMasterId = Integer.parseInt(tblClassMaster.getModel().getValueAt(tblClassMaster.getSelectedRow(),tblClassMaster.getColumnCount()-1).toString());
        ClassMasterDAO classMasterDAO = new ClassMasterDAO();
        classMaster = classMasterDAO.retrieveById(classMasterId);

        txtFees.setText(classMaster.getFees()+"");
        txtClassName.setText(classMaster.getClassName());
        enableFormElements(true);
        btnSave.setText("Update");
        btnSave.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/update.png")));
    }

