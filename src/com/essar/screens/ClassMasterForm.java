    @Override
    public void actionPerformed(ActionEvent event) {
        try{
            JMenuItem menu = (JMenuItem) event.getSource();
            if (menu == mitDelete) {
                //int rowIndex = tblCategory.getSelectedRow();
                //System.out.println("--------INSIDE DELETE-"+tblCategory.getModel().getValueAt(tblCategory.getSelectedRow(),0));
                String classMasterCode = tblClassMaster.getModel().getValueAt(tblClassMaster.getSelectedRow(),0).toString();
                ClassMasterDAO classMasterDAO = new ClassMasterDAO();
                classMasterDAO.deleteRecordByCode(classMasterCode);
                //initializeForm();
                GenericUtils.removeRowFromJTable(tblClassMaster, classMasterModel);
            } else if (menu == mitEdit) {
                syncDataFromViewToTable();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }
